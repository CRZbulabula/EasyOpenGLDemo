double getRandom()
{
    double A = (double)rand() / 6553600.0f;
    int B = rand() % 2;
    if (B) {
        return A + 0.005f;
    } else {
        return -A - 0.005f;
    }
}

void randomVelocity()
{
    velocity.x = getRandom();
    velocity.y = getRandom();
    velocity.z = getRandom();
}

void aabbCollision()
{
    glm::vec3 pos = cubePositions[0];
    if (pos.x - 0.5f <= -border || pos.x + 0.5f >= border) {
        velocity.x *= -1.0f;
    }
    if (pos.y - 0.5f <= -border || pos.y + 0.5f >= border) {
        velocity.y *= -1.0f;
    }
    if (pos.z - 0.5f <= -border || pos.z + 0.5f >= border) {
        velocity.z *= -1.0f;
    }
}